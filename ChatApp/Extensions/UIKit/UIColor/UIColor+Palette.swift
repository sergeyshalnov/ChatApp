import UIKit


// MARK: - Palette extension

extension UIColor {
  
  enum Palette {
    enum Blue {
      static let online = UIColor.init(hex: "#F1F6FE")
    }
    enum Bubble {
      static let grey = UIColor.init(hex: "#D2D2D6")
      static let blue = UIColor.init(hex: "#4E9FF5")
    }
  }
  
}
